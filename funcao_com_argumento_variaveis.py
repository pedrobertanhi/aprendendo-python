# Suporta numeros varieveis de argumentos
# Utiliza o operador * antes do nome
def exibe_promocao(*cliente):
    for cliente in cliente:
        print(f"Olá {cliente}!\n queremos te informar sobre nossa promoção de fim de ano!")
    
lista_clientes = ["Ana", "Maria", "João", "Bruno", "Carla"]
exibe_promocao(*lista_clientes)  # Desempacotando a lista

